import re

def solve(text):
    # Регулярное выражение для времени в формате HH:MM или HH:MM:SS
    time_pattern = r'\b([01]\d|2[0-3]):([0-5]\d)(?::([0-5]\d))?\b'
    #\b — указывает на границу слова, чтобы избежать захвата частей других слов.
    #([01]\d|2[0-3]) — находит часы. Часы могут быть от 00 до 23.
    #(2[0-3]) — часы от 20 до 23.
    #:([0-5]\d) — находит минуты. Минуты — числа от 00 до 59.
    #(?::([0-5]\d))? — опционально захватывает секунды (если есть), также от 00 до 59.
    #\b — закрывает границу слова.
    # Замена всех вхождений времени на "TBD"
    updated_text = re.sub(time_pattern, '(TBD)', text)
    return updated_text
